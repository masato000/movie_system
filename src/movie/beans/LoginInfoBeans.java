package movie.beans;

public class LoginInfoBeans {

	private String name;
	private String mail;
	private String pass;


	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getPass() {
		return pass;
	}
	public void setClassName(String pass) {
		this.pass = pass;
	}
	


} 